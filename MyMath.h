	Matrix4x4 rotationMatrix;

	rotationMatrix.m[0][0] = c;
	rotationMatrix.m[0][1] = s;
	rotationMatrix.m[0][2] = 0.0f;
	rotationMatrix.m[0][3] = 0.0f;
	rotationMatrix.m[1][0] = -s;
	rotationMatrix.m[1][1] = c;
